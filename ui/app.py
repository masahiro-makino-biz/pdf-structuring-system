import streamlit as st
import requests
import os

# =============================================================================
# 設定
# =============================================================================
# 【環境変数とは】
# プログラムの外から渡される設定値。docker-compose.ymlで定義している
# os.getenv("API_URL", "http://localhost:8000") の意味：
#   - 環境変数 API_URL があればその値を使う
#   - なければデフォルト値 "http://localhost:8000" を使う
API_URL = os.getenv("API_URL", "http://localhost:8000")

# =============================================================================
# ページ設定
# =============================================================================
# 【st.set_page_config】ページ全体の設定
# 【注意】この関数はスクリプトの最初に呼ぶ必要がある（他のst.xxx より先）
#
# 【引数の意味】
#   - page_title: ブラウザのタブに表示されるタイトル
#   - page_icon: タブに表示されるアイコン（絵文字が使える）
#   - layout: "wide" で画面幅いっぱいに表示、"centered" で中央寄せ
st.set_page_config(
    page_title="PDF構造化システム",
    page_icon="📄",
    layout="wide",
)

# =============================================================================
# サイドバー - ページ選択
# =============================================================================
# 【st.sidebar】画面左側のサイドバーに要素を配置
# 【st.radio】ラジオボタン（選択肢から1つ選ぶUI）
#
# 【なぜサイドバーにページ選択を置くか】
#   - 常に表示されているので、ページ移動がしやすい
#   - メイン画面を広く使える
#
# 【他の方法】
#   - st.tabs: タブ形式。1ページ内で切り替える場合に適している
#   - pages/フォルダ: Streamlitの標準マルチページ機能。ファイルを分ける
st.sidebar.title("📄 PDF構造化システム")
page = st.sidebar.radio(
    "ページ選択",
    ["🔧 Admin - PDF管理", "💬 User - チャット"],
    label_visibility="collapsed",  # ラベル「ページ選択」を非表示に
)


# =============================================================================
# Admin画面 - PDF管理
# =============================================================================
def admin_page():
    """
    管理者用ページ：PDFのアップロード、ジョブ管理、結果確認

    【関数に分ける理由】
    - コードが長くなるので、ページごとに関数に分割
    - 可読性が上がる
    - 他のページと変数名がぶつからない
    """
    st.title("🔧 Admin - PDF管理")

    # -------------------------------------------------------------------------
    # PDFアップロードセクション
    # -------------------------------------------------------------------------
    st.header("📤 PDFアップロード")

    # 【st.file_uploader】ファイルアップロードのUI
    # 【引数の意味】
    #   - "PDFファイルを選択": 表示されるラベル
    #   - type=["pdf"]: 受け付けるファイルの拡張子
    #   - help: ホバー時に表示されるヘルプテキスト
    uploaded_file = st.file_uploader(
        "PDFファイルを選択",
        type=["pdf"],
        help="アップロードするPDFファイルを選んでください",
    )

    # 【st.columns】画面を横に分割
    # col1, col2 = st.columns(2) で2列に分割
    # 引数 [2, 1] で、左:右 = 2:1 の比率
    col1, col2 = st.columns([2, 1])

    with col1:
        # 【st.text_input】テキスト入力欄
        tenant_id = st.text_input(
            "テナントID",
            value="default",
            help="データを分けるための識別子。通常はそのまま「default」でOK",
        )

    with col2:
        # 【st.button】ボタン
        # 【disabled】条件を満たさない時にボタンを無効化
        upload_button = st.button(
            "アップロード実行",
            type="primary",  # 青色の目立つボタン
            disabled=(uploaded_file is None),  # ファイルが選択されていないと無効
        )

    # ボタンがクリックされた時の処理
    if upload_button and uploaded_file:
        # 【st.spinner】処理中の表示（くるくる回るアニメーション）
        with st.spinner("アップロード中..."):
            # フェーズ2で実装：APIにファイルを送信
            # 今はメッセージだけ表示
            st.info(
                f"📁 ファイル「{uploaded_file.name}」を受け取りました\n\n"
                "⚠️ API連携はフェーズ2で実装します"
            )

    # -------------------------------------------------------------------------
    # ジョブ一覧セクション（プレースホルダー）
    # -------------------------------------------------------------------------
    st.header("📋 処理ジョブ一覧")
    st.info("フェーズ2で実装：ジョブの状態（処理中/完了/エラー）を表示します")

    # 【st.empty()】後から内容を入れ替えられるプレースホルダー
    # 今は空のテーブルを表示
    st.dataframe(
        {
            "ジョブID": ["（なし）"],
            "ファイル名": ["-"],
            "状態": ["-"],
            "作成日時": ["-"],
        },
        use_container_width=True,  # 画面幅に合わせる
    )


# =============================================================================
# User画面 - チャット
# =============================================================================
def user_page():
    """
    ユーザー用ページ：構造化データに対してチャットで質問

    【Streamlitのチャット機能】
    st.chat_message, st.chat_input を使うと、
    ChatGPTのようなUIを簡単に作れる
    """
    st.title("💬 User - チャット")

    # -------------------------------------------------------------------------
    # セッションステートでチャット履歴を管理
    # -------------------------------------------------------------------------
    # 【st.session_state とは】
    # Streamlitは画面更新のたびにスクリプト全体が再実行される
    # 通常の変数は再実行のたびにリセットされてしまう
    # session_state を使うと、再実行をまたいでデータを保持できる
    #
    # 【なぜ必要か】
    # チャット履歴を覚えておくため
    # session_stateがないと、メッセージ送信のたびに履歴が消える
    if "messages" not in st.session_state:
        st.session_state.messages = []

    # -------------------------------------------------------------------------
    # チャット履歴の表示
    # -------------------------------------------------------------------------
    # 【st.chat_message】チャットの吹き出しを表示
    # role="user" でユーザー側、role="assistant" でAI側のスタイル
    for message in st.session_state.messages:
        with st.chat_message(message["role"]):
            st.markdown(message["content"])

    # -------------------------------------------------------------------------
    # チャット入力
    # -------------------------------------------------------------------------
    # 【st.chat_input】チャット入力欄（画面下部に固定表示）
    # ユーザーがEnterキーを押すか送信ボタンを押すと、入力内容が返される
    if prompt := st.chat_input("質問を入力してください"):
        # 【:= （ウォルラス演算子）】
        # 代入と条件判定を同時に行う Python 3.8 以降の構文
        # 上の行は以下と同じ意味：
        #   prompt = st.chat_input("質問を入力してください")
        #   if prompt:

        # ユーザーのメッセージを履歴に追加
        st.session_state.messages.append({"role": "user", "content": prompt})

        # ユーザーのメッセージを表示
        with st.chat_message("user"):
            st.markdown(prompt)

        # AIの応答（フェーズ4で実装。今はダミー応答）
        with st.chat_message("assistant"):
            # 【st.spinner】処理中の表示
            with st.spinner("考え中..."):
                # フェーズ4で実装：APIにリクエストを送信
                response = f"（フェーズ4で実装）質問「{prompt}」を受け取りました。"
                st.markdown(response)

        # AIの応答を履歴に追加
        st.session_state.messages.append({"role": "assistant", "content": response})


# =============================================================================
# ページのルーティング
# =============================================================================
# 【ルーティングとは】
# どのURLでどのページを表示するかを決める仕組み
# ここではサイドバーの選択に応じて、表示する関数を切り替えている
if "Admin" in page:
    admin_page()
else:
    user_page()


# =============================================================================
# APIヘルスチェック（サイドバー下部）
# =============================================================================
# 【なぜヘルスチェックを表示するか】
# APIが動いているか一目で分かるようにするため
# 開発中のトラブルシューティングに便利
st.sidebar.divider()  # 区切り線
st.sidebar.caption("API接続状態")

try:
    # 【requests.get】HTTPのGETリクエストを送信
    # 【timeout=2】2秒以内に応答がなければタイムアウト
    response = requests.get(f"{API_URL}/health", timeout=2)
    if response.status_code == 200:
        st.sidebar.success("✅ API接続OK")
    else:
        st.sidebar.error(f"❌ API異常 (status: {response.status_code})")
except requests.exceptions.RequestException:
    # 【例外処理】通信エラー時の処理
    st.sidebar.error("❌ API接続失敗")
