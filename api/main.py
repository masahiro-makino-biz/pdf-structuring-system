# =============================================================================

from fastapi import FastAPI
from fastapi.middleware.cors import CORSMiddleware

# =============================================================================
# FastAPIアプリケーションの作成
# =============================================================================
# 【FastAPI()とは】
# アプリケーションのインスタンスを作成する
# このappオブジェクトに対してエンドポイントを追加していく
#
# 【引数の意味】
#   - title: APIドキュメント（/docs）に表示されるタイトル
#   - version: APIのバージョン
#   - description: APIの説明文
app = FastAPI(
    title="PDF構造化API",
    version="0.1.0",
    description="PDFをアップロードして構造化データに変換するAPI",
)

# =============================================================================
# CORSミドルウェアの設定
# =============================================================================
# 【CORSとは】
# Cross-Origin Resource Sharing（クロスオリジンリソース共有）
# 異なるドメイン（例：localhost:8501 → localhost:8000）からのアクセスを許可する設定
#
# 【なぜ必要か】
# ブラウザはセキュリティのため、異なるドメインへのリクエストをデフォルトでブロックする
# StreamlitとFastAPIはポートが違うため、CORSを設定しないと通信できない
#
# 【注意】
# allow_origins=["*"] は開発用。本番環境では特定のドメインのみ許可すべき
app.add_middleware(
    CORSMiddleware,
    allow_origins=["*"],  # 全てのオリジンを許可（開発用）
    allow_credentials=True,
    allow_methods=["*"],  # 全てのHTTPメソッドを許可
    allow_headers=["*"],  # 全てのヘッダーを許可
)


# =============================================================================
# エンドポイントの定義
# =============================================================================


@app.get("/")
async def root():
    """
    ルートエンドポイント（動作確認用）

    【デコレータ @app.get("/") とは】
    - @app → 上で作ったFastAPIインスタンス
    - .get → HTTPのGETメソッド（データを取得する時に使う）
    - ("/") → URLのパス（http://localhost:8000/ でアクセス）

    【async def とは】
    非同期関数の宣言。FastAPIでは async def を使うのが推奨
    理由：複数のリクエストを効率よく処理できる

    【他のHTTPメソッド】
    - GET: データの取得（例：ユーザー情報を取得）
    - POST: データの作成（例：新規ユーザー登録）
    - PUT: データの更新（例：ユーザー情報の変更）
    - DELETE: データの削除（例：ユーザーの削除）
    """
    return {"message": "PDF構造化API is running", "status": "ok"}


@app.get("/health")
async def health_check():
    """
    ヘルスチェックエンドポイント

    【なぜ必要か】
    サーバーが正常に動いているか確認するためのエンドポイント
    Docker等のコンテナオーケストレーションで自動監視に使う

    【戻り値について】
    辞書（dict）を返すと、FastAPIが自動でJSON形式に変換してくれる
    """
    return {"status": "healthy"}


